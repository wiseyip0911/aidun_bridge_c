const RICH_RE = /\[(?:bold\s+)?(?:dim\s+)?(#(?:[0-9a-fA-F]{3,8}))\]([\s\S]*?)(\[\/\])/g

export function parseRichMarkup(markup: string): Line[] {
  const lines: Line[] = []
  const full = markup.replace(/\r\n/g, '\n')

  const pushPlainBlock = (block: string) => {
    for (const raw of block.split('\n')) {
      const trimmed = raw.trimEnd()

      if (trimmed) {
        lines.push(['', trimmed])
      }
    }
  }

  let lastIndex = 0

  for (const m of full.matchAll(RICH_RE)) {
    const idx = m.index ?? 0
    const before = full.slice(lastIndex, idx)

    if (before) {
      pushPlainBlock(before)
    }

    const color = m[1]!
    const body = m[2] ?? ''
    const parts = body.split('\n')

    if (parts.length === 0 || (parts.length === 1 && parts[0] === '')) {
      lines.push([color, ' '])
    } else {
      for (const raw of parts) {
        const trimmed = raw.trimEnd()

        lines.push([color, trimmed.length > 0 ? trimmed : ' '])
      }
    }

    lastIndex = idx + m[0].length
  }

  const tail = full.slice(lastIndex)

  if (tail) {
    pushPlainBlock(tail)
  }

  if (lines.length === 0) {
    lines.push(['', ' '])
  }

  return lines
}

